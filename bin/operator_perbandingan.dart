void main() {
  //Sama dengan
  print(10 == 10);
  //Tidak Sama dengan
  print(10 != 10);
  //Lebih dari
  print(10 > 10);
  //Kurang dari
  print(10 == 10);
  //Lebih dari sama dengan
  print(10 >= 10);
  //Kurang dari sama dengan
  print(10 == 10);

  //Atau kita bisa bandingkan dengan string
  print('abie' == 'abie');
  print('abie' != 'abie');
  // print('abie' > 'abie'); // Kalau lebih dari tidak bisa
}
