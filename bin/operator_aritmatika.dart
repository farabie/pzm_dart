void main() {
  var number1 = 12;
  var number2 = 3;

  //Perjumlahan
  print(number1 + number2);
  //Pengurangan
  print(number1 - number2);
  //Angka Negatif
  print(-number1);
  //Angka positif
  print(number2);
  //Perkalian
  print(number1 * number2);
  //Pembagian hasilnya double
  print(number1 / number2);
  //Pembagian hasilnya int
  print(number1 ~/ number2);
  //Sisa bagi
  print(number1 % number2);
}
